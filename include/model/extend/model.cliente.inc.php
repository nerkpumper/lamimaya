		public function updateMontoCreditoUsingDatosFacturacion($idCliente)
		{
			$query = "SELECT IFNULL(SUM(credito), 0) as credito
						FROM clientedatosfacturacion
						INNER JOIN datosfacturacion 
						ON datosfacturacion.idDatosFacturacion = datosfacturacion.idDatosFacturacion
						WHERE clientedatosfacturacion.idCliente = " . $idCliente;

