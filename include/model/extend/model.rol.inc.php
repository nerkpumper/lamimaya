		public function getAllIdRolTitulo()
		{
			$query="SELECT * FROM rol";
			$result=mysqli_query($this->dbLink, $query);
			if(!$result)
			{
				return $this->setSystemError("Ocurrio un error en la busqueda de los roles activos.", "[" . $this->_nombreClase . ":LN50][" . $query . "][" . mysqli_error($this->dbLink) . "]");	
			}			
			$datos=array();
			while($row=mysqli_fetch_assoc($result))
			{
				$datos[$row['idRol']]=$row['titulo'];
			}
			return $datos;
		}

