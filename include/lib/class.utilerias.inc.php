	public static function formatoFecha($fecha){
// 		return $fecha;
		$f = substr($fecha, 0, 10);
		$formated = substr($f, 8, 2) . "/" . substr($f, 5,2) . "/" . substr($f, 0,4);
		
		$formated .= " " . substr($fecha, 11);
		$formated = trim($formated);
		
		return $formated;
