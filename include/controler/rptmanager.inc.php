	function cargarParents()
	{
		$r = new xajaxResponse();
		
		ReportManager::fillLista();
		
		$lstPadres = ReportManager::getParents();
		
		$pushes = "";
		foreach ( $lstPadres as $parent ) 
		{		
			if (Routes::moduleAllow ( $parent ["id"] )) 
			{			
				$pushes .= "
						app.categorias.push({
							id:     '".$parent["id"]."',
							nombre: '".$parent["nombre"]."',
							icon:   '".$parent["icon"]."'			
						});
						
						";
			}
		}
		
		$r->script(" app.categorias.splice(0, app.categorias.length);" . $pushes);		
		
		return $r;
	}
	$xajax->registerFunction("cargarParents");
	
	function cargarHijos($parent)
	{
		$r = new xajaxResponse();
	
		ReportManager::fillLista();
	
		$lstPadres = ReportManager::getChildren($parent);
	
		$pushes = "";
		foreach ( $lstPadres as $child )
		{
			if (Routes::moduleAllow ( $child ["id"] ))
			{
				$pushes .= "
						app.children.push({
							id:     '".$child["id"]."',
							nombre: '".$child["nombre"]."',
							icon:   '".$child["icon"]."'
						});
	
						";
			}
		}
	
		$r->script(" app.children.splice(0, app.children.length);" . $pushes);
	
		return $r;
	}
	$xajax->registerFunction("cargarHijos");
	
	$xajax->processRequest();

