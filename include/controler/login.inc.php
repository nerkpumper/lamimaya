	function ingresar ($email, $pass)
	{
		global $dbLink;
		$r = new xajaxResponse();
		$email = trim($email);
		$pass = trim($pass);
		
		if ($email == "")
		{
			$r->mostrarAviso("Ingresa tu email.");
			return $r;
		}
		if ($pass == "")
		{
			$r->mostrarAviso("Ingresa tu contraseña.");
			return $r;
		}
		
		
		
		if (login($email, $pass, $dbLink) == true)
		{	
			// Inicio de sesi�n exitosa
			$objSession=new ModeloUsuario($dbLink);
 			$objSession->setIdUsuario($_SESSION['idUsuariolm']);
		
			$_SESSION['_lmobjSession']=serialize($objSession);			
			$r->redirect(URL_BASE . "index",2);
// 			$r->mostrarAviso($objSession->getSQLGetDatos());
			
			
		}
		else
		{			
			// No inicio de sesion

			$r->mostrarError("Error en los datos de ingreso.");
			return $r;
		}		
		
		
		return $r;
	}
	$xajax->registerFunction("ingresar");
		
	

	$xajax->processRequest();

