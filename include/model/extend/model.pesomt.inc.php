		public function getDatosByCalibrePies($calibre)
		{
			try
			{
				$SQL="SELECT
						idPesoMt,
						calibre,
						pies2,
						pies3,
                        pies348,
                        pies376,
						pies4,
						fecha_modificacion,
						id_usuario_modificacion
					FROM " . $this->__tableName . "
					WHERE calibre =" . mysqli_real_escape_string($this->dbLink,$calibre);
					
				$result=mysqli_query($this->dbLink,$SQL);
				if(!$result)
					return $this->setSystemError("Error en la obtencion de detalles de registro.","[ModeloBasePesomt::getDatos][" . $SQL . "][" . mysqli_error($this->dbLink) . "]");
		
		
					if(mysqli_num_rows($result)==0)
					{
						$this->limpiarPropiedades();
					}
					else
					{
						$datos=mysqli_fetch_assoc($result);
						foreach($datos as $k=>$v)
						{
							$campo="" . $k;
							$this->$campo=$v;
						}
					}
					return true;
			}
			catch (Exception $e)
			{
				return $this->setErrorCatch($e);
			}
		}
		
