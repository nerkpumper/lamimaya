	function cambiaPassword ($passActual, $passNew, $confirma)
	{		
		$r = new xajaxResponse();
				
		$user = new ModeloUsuario();

		$user->setIdUsuario(ModeloUsuario::getObjSession()->getIdUsuario());

		$passwordCheck = hash('sha512', $passActual . $user->getSalt());
		
		if ($user->getPassword() != $passwordCheck)
		{
			$r->saError("Ingrese su password correcto.");
		}			
		else
		{
			$passwordNuevo = $passNew;
			
			$passwordSalt = hash('sha512', $passwordNuevo . $user->getSalt());
			
			$user->setPassword($passwordSalt);
			
			$user->Guardar();
			
			if ($user->getError())
			{
				$r->saError($user->getStrError());
				return $r;
			}
			else
			{			
				$r->saSuccess("El password se ha modificado satisfactoriamente." );
				$r->redirect(URL_BASE . "miperfil",1);
			}
		}
		
		return $r;
	}
	$xajax->registerFunction("cambiaPassword");
		
	

	$xajax->processRequest();

